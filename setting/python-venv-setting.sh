# 프로젝트 root 진입
$ cd [PROJECT_NM]

# 가상환경 생성
$ python3 -m venv [VIRTUAL_ENV_NM]

# 가상환경 활성화
$ source [VIRTUAL_ENV_NM]/bin/activate

# 가상환경 비활성화
$ deactivate

